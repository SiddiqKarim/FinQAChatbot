def preprocess_text(text):
    doc = nlp(text)
    lemmas = [token.lemma_ for token in doc]
    lemmas_without_stopwords = [lemma for lemma in lemmas if lemma.lower() not in stopwords and lemma.isalpha()]

    return ' '.join(lemmas_without_stopwords)

